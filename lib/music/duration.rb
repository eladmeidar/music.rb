module Music
  module Duration
    WN  = 4        # whole note
    DHN = 3        # dotted half
    HN  = 2        # half
    DQN = 3.quo 2  # dotted quarter
    QN  = 1        # quarter
    DEN = 3.quo 4  # dotted eighth
    EN  = 1.quo 2  # eighth
    TEN = 1.quo 3  # triplet eighth
    DSN = 3.quo 8  # dotted sixteenth
    SN  = 1.quo 4  # sixteenth
    TSN = 1.quo 6  # triplet sixteenth
    DTN = 3.quo 16 # dotted thirty-second
    TN  = 1.quo 8  # thirty-second
    TTN = 1.quo 9  # triplet thirty-second
  end
end
